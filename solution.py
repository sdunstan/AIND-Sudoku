from collections import defaultdict

assignments = []

def assign_value(values, box, value):
    """
    Please use this function to update your values dictionary!
    Assigns a value to a given box. If it updates the board record it.
    """
    values[box] = value
    if len(value) == 1:
        assignments.append(values.copy())
    return values

def naked_twins(values):
    """Eliminate values using the naked twins strategy.
    Args:
        values(dict): a dictionary of the form {'box_name': '123456789', ...}

    Returns:
        the values dictionary with the naked twins eliminated from peers.
    """
    # Find all instances of naked twins
    naked_twins_dict = defaultdict(list)
    naked_twin_candidates = [box for box in values.keys() if len(values[box]) == 2]
    for naked_twin_candidate in naked_twin_candidates:
        for unit in units[naked_twin_candidate]:
            box_count = sum(1 for box in unit if values[naked_twin_candidate] == values[box])
            if box_count >= 2: # found one other besides myself
                naked_twins_dict[naked_twin_candidate].append(unit)

    # Eliminate values from other boxes in the naked twin units
    for naked_twin, naked_units in naked_twins_dict.items():
        for unit in naked_units:
            for peer in unit:
                if values[peer] != values[naked_twin]:  # except the naked twins
                    for char in values[naked_twin]:
                        if char in values[peer]:
                            assign_value(values, peer, values[peer].replace(char, ''))

    return values

def cross(vectorLeft, vectorRight):
    "Cross product of elements in A and elements in B."
    return [s+t for s in vectorLeft for t in vectorRight]

